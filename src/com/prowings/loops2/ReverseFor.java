package com.prowings.loops2;

public class ReverseFor {
	
	public static void main(String[] args) {
		
		for(int i=5; i>=1; i--)
		{
			System.out.println(i);
		}
		
		System.out.println("===============================");
		
		int[] numbers = {10,20,30,40,50};
		
		for(int i=0; i<numbers.length; i++)
		{
			System.out.println(numbers[i]);
		}
		
	}

}
